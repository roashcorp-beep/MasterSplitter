const VALID_LANGUAGES = ['he', 'en', 'es', 'ru', 'ar', 'fr', 'zh'];
const RTL_LANGUAGES = ['he', 'ar'];

let currentLang = 'he';

function initLanguage() {
    let saved = localStorage.getItem('lang');
    if(saved) setLanguage(saved);
    else applyTranslations(); // fallback to default 'he'

    // We fetch language from API to sync
    fetch('/api/me').then(res => {
        if(res.ok) {
            res.json().then(data => {
                if(data.language && data.language !== saved) {
                    setLanguage(data.language);
                }
            });
        }
    }).catch(e => {
        console.error('Failed to sync language from API', e);
    });
}

function setLanguage(lang) {
    if (!VALID_LANGUAGES.includes(lang)) lang = 'he';
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
    const isRTL = RTL_LANGUAGES.includes(lang);
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.body.style.direction = isRTL ? 'rtl' : 'ltr';
    applyTranslations();
    if (typeof window.updateUI === 'function') window.updateUI();
    window.dispatchEvent(new Event('languageChanged'));
}

function i18n(key) {
    if (!translations[currentLang]) return translations['en']?.[key] || key;
    return translations[currentLang][key] !== undefined ? translations[currentLang][key] : (translations['en']?.[key] || key);
}

function applyTranslations() {
    const langData = translations[currentLang] || translations['en'] || {};
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (langData[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = langData[key];
            } else {
                el.textContent = langData[key];
            }
        }
    });
    
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (langData[key]) {
            el.innerHTML = langData[key];
        }
    });
    const isRTL = RTL_LANGUAGES.includes(currentLang);
    document.querySelectorAll('input, textarea').forEach(el => {
        if(el.type !== 'hidden' && el.type !== 'checkbox' && el.type !== 'radio' && el.type !== 'file') {
            el.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
        }
    });
}

// Expose translations to window for React components
window.translations = translations;

// Call init on load
document.addEventListener('DOMContentLoaded', () => {
    initLanguage();
});
